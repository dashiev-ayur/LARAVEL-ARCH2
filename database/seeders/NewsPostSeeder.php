    /**
     * Run the database seeds.
     */
    public function run(): void
    {
        Org::query()->find(self::ORG_ID) ?? Org::factory()->create([
            'id' => self::ORG_ID,
            'name' => 'Seed News Org',
            'slug' => 'seed-news-org',
            'status' => 'enabled',
        ]);

        $author = User::query()->first() ?? User::factory()->create();

        foreach (range(1, self::NEWS_COUNT) as $number) {
            $example = self::NEWS_EXAMPLES[($number - 1) % count(self::NEWS_EXAMPLES)];

            Post::factory()->create([
                'org_id' => self::ORG_ID,
                'author_id' => $author->id,
                'type' => PostType::News,
                'status' => 'published',
                'slug' => Str::slug($example['title']).'-'.str_pad((string) $number, 3, '0', STR_PAD_LEFT),
                'title' => $example['title'],
                'excerpt' => $example['excerpt'],
                'content' => $example['content'],
                'published_at' => now()->subDays(self::NEWS_COUNT - $number),
            ]);
        }
    }
